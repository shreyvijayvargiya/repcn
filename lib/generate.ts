function slugify(name: string) {
  return (
    name
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, '-')
      .replace(/^-|-$/g, '')
      .slice(0, 40) || 'repcn-app'
  )
}

function extractComponentName(code: string): string {
  const named =
    code.match(/export\s+default\s+function\s+([A-Za-z0-9_]+)/)?.[1] ||
    code.match(/function\s+([A-Za-z0-9_]+)\s*\(/)?.[1] ||
    code.match(/const\s+([A-Za-z0-9_]+)\s*=/)?.[1]
  return named || 'GeneratedComponent'
}

function ensureDefaultExport(code: string, componentName: string): string {
  if (/export\s+default/.test(code)) return code.trim() + '\n'
  if (
    new RegExp(`function\\s+${componentName}\\b`).test(code) ||
    new RegExp(`const\\s+${componentName}\\b`).test(code)
  ) {
    return `${code.trim()}\n\nexport default ${componentName}\n`
  }
  return `${code.trim()}\n\nexport default function ${componentName}() {\n  return null\n}\n`
}

function commentBlock(instructions: string) {
  if (!instructions.trim()) return ''
  return `\n{/* Instructions: ${instructions.replace(/\*\//g, '* /')} */}\n`
}

function componentExt(language: LanguageId) {
  return language === 'js' ? 'jsx' : 'tsx'
}

function scriptExt(language: LanguageId) {
  return language === 'js' ? 'js' : 'ts'
}

function sharedTailwindFiles(
  language: LanguageId,
  contentGlobs: string[]
): RepoFile[] {
  const postcss: RepoFile = {
    path: 'postcss.config.mjs',
    content: `/** @type {import('postcss-load-config').Config} */
const config = {
  plugins: {
    tailwindcss: {},
    autoprefixer: {},
  },
}

export default config
`,
  }

  if (language === 'js') {
    return [
      postcss,
      {
        path: 'tailwind.config.js',
        content: `/** @type {import('tailwindcss').Config} */
export default {
  content: ${JSON.stringify(contentGlobs, null, 2)},
  theme: {
    extend: {},
  },
  plugins: [],
}
`,
      },
    ]
  }

  return [
    postcss,
    {
      path: 'tailwind.config.ts',
      content: `import type { Config } from 'tailwindcss'

const config: Config = {
  content: ${JSON.stringify(contentGlobs, null, 2)},
  theme: {
    extend: {},
  },
  plugins: [],
}

export default config
`,
    },
  ]
}

function readme(
  repoName: string,
  frameworkLabel: string,
  language: LanguageId,
  instructions: string,
  runCommand: string,
  shadcn: boolean
): RepoFile {
  const langLabel = language === 'js' ? 'JavaScript' : 'TypeScript'
  const extHint = language === 'js' ? '.js / .jsx' : '.ts / .tsx'
  return {
    path: 'README.md',
    content: `# ${repoName}

Generated by RepCN as a **${frameworkLabel}** (${langLabel}) app from a React component.

File extensions: \`${extHint}\`
${shadcn ? '\nIncludes **shadcn/ui** with `Button` and `Input` under `components/ui`.\n' : ''}
${instructions ? `## Instructions\n\n${instructions}\n\n` : ''}## Run

\`\`\`bash
${runCommand}
\`\`\`
`,
  }
}

function pkgJson(
  repoName: string,
  packages: PackageEntry[],
  language: LanguageId,
  scripts: Record<string, string>,
  extra?: Record<string, unknown>
): RepoFile {
  return {
    path: 'package.json',
    content:
      JSON.stringify(
        {
          name: repoName,
          version: '0.1.0',
          private: true,
          ...extra,
          scripts,
          dependencies: packagesToRecord(packages, 'dependency', language),
          devDependencies: packagesToRecord(packages, 'devDependency', language),
        },
        null,
        2
      ) + '\n',
  }
}

function styleAndUiFiles(
  framework: FrameworkId,
  language: LanguageId,
  contentGlobs: string[],
  cssPath: string,
  plainCss: string,
  shadcn: boolean
): RepoFile[] {
  const postcss: RepoFile = {
    path: 'postcss.config.mjs',
    content: `/** @type {import('postcss-load-config').Config} */
const config = {
  plugins: {
    tailwindcss: {},
    autoprefixer: {},
  },
}

export default config
`,
  }

  if (shadcn) {
    return [postcss, ...generateShadcnFiles({ framework, language, contentGlobs })]
  }

  return [
    ...sharedTailwindFiles(language, contentGlobs),
    { path: cssPath, content: plainCss },
  ]
}

function pageContent(opts: {
  language: LanguageId
  shadcn: boolean
  note: string
  componentName: string
  importLine: string
  fnName?: string
}): string {
  const fn = opts.fnName || 'HomePage'
  const imports = opts.shadcn
    ? `${shadcnDemoImports()}\n${opts.importLine}`
    : opts.importLine
  const helper = opts.shadcn ? `\n${shadcnDemoComponent()}\n` : '\n'
  const starter = opts.shadcn ? '      <ShadcnStarter />\n' : ''
  return `${imports}
${helper}export default function ${fn}() {
  return (
    <main className="mx-auto min-h-screen max-w-3xl p-6">
${starter}      ${opts.note}<${opts.componentName} />
    </main>
  )
}
`
}

