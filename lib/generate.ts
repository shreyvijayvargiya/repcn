export function generateRepoFiles(input: GenerateInput): {
  repoName: string
  files: RepoFile[]
  framework: FrameworkId
  language: LanguageId
  shadcn: boolean
} {
  const framework: FrameworkId = input.framework || 'nextjs'
  const language: LanguageId = input.language || 'ts'
  const shadcn = Boolean(input.shadcn)
  const repoName = slugify(input.repoName || 'repcn-app')
  const componentName = extractComponentName(input.componentCode)
  const componentSource = ensureDefaultExport(
    input.componentCode,
    componentName
  )
  const instructions = (input.instructions || '').trim()
  const payload: GenCtx = {
    repoName,
    componentName,
    componentSource,
    instructions,
    language,
    shadcn,
  }

  const files =
    framework === 'vite'
      ? generateVite(payload)
      : framework === 'tanstack'
        ? generateTanstack(payload)
        : generateNextjs(payload)

  return { repoName, files, framework, language, shadcn }
}

export function summarizeRepo(files: RepoFile[]) {
  const encoder = new TextEncoder()
  return files.map((f) => ({
    path: f.path,
    bytes: encoder.encode(f.content).length,
  }))
}

export type TreeNode = {
  name: string
  path: string
  type: 'file' | 'folder'
  children?: TreeNode[]
}

/** Build a nested folder tree from flat file paths. */
export function buildFileTree(files: { path: string }[]): TreeNode[] {
  type Mutable = {
    name: string
    path: string
    type: 'file' | 'folder'
    children?: Map<string, Mutable>
  }

  const root = new Map<string, Mutable>()

  for (const file of files) {
    const parts = file.path.split('/')
    let current = root
    let pathSoFar = ''

    for (let i = 0; i < parts.length; i++) {
      const part = parts[i]
      pathSoFar = pathSoFar ? `${pathSoFar}/${part}` : part
      const isFile = i === parts.length - 1

      if (!current.has(part)) {
        current.set(part, {
          name: part,
          path: pathSoFar,
          type: isFile ? 'file' : 'folder',
          children: isFile ? undefined : new Map(),
        })
      }

      const node = current.get(part)!
      if (!isFile) {
        if (!node.children) node.children = new Map()
        current = node.children
      }
    }
  }

  function toArray(map: Map<string, Mutable>): TreeNode[] {
    return [...map.values()]
      .map((node) => ({
        name: node.name,
        path: node.path,
        type: node.type,
        children: node.children ? toArray(node.children) : undefined,
      }))
      .sort((a, b) => {
        if (a.type !== b.type) return a.type === 'folder' ? -1 : 1
        return a.name.localeCompare(b.name)
      })
  }

  return toArray(root)
}
